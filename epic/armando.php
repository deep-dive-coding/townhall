		<p><strong>Attitudes and Behaviors:</strong> Armando is native Burqueno with a green-chile-like passion for his
			hometown. This passion fuels his career choice as well as his work ethic. Although his credentials could land
			him a higher government position, he prefers his council-ship as it allows him to work directly with his
			constituents.</p>


		<p><strong>Frustrations and Needs:</strong> Armando is constantly juggling requests from businesses and his
			constituents. He has a PR analyst Gene who he uses sparingly to help with communications when he has too much
			on his plate. Constrained by the city's dwindling budget, Armando is constantly searching for smarter solutions
			to satisfy his district's requests.</p>


		<p><strong>Goals:</strong> Armando is always curious how his policies and statements are interpreted by his
			consituents. He hosts bi-monthly townhalls but the hours aren't accessible to a large portion of his
			constituents. Ideally, Armando would have a communication tool that works with everybody schedule so he
			effectively address the issues important to his community.</p>


		<p><strong>User Story:</strong> One of the most arduous aspects of my job is determining how my constituents
			interpret my policies; some reveal this sentiment through social media but I find it can be a bit
			biased. I need a forum all of them could access and leave their questions and criticisms for me.</p>
